# # Ruby Inheritance.
# Inheritance is a relation between two classes. 
# If a programmer wants to create a new class and there is a class that already includes some of the code 
# then he or she can derive a new class from the existing class. 
# By doing this, it increases the reuse of the fields and methods of the existing class without creating extra code.


#  Inheritance allows you to create a class that is a refinement
#  or specialization of another class. 
#  Inheritance is indicated with <.
#  A class can only inherit from one class at a time

# Key terms in Inheritance:

# Super class:The class whose characteristics are inherited is known as a superclass or base class or parent class
# # Sub class:The class which is derived from another class is known as a subclass or child class. 





